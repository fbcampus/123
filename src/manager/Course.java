package manager;

public class Course {
	//定义课程的ID
	private String courseId;
	//定义课程的名称
	private String courseName;
	//定义课程的学分
	private String courseScore;
	//定义课程的理论学时
	private String courseTheory;
	//定义课程的实验学时
	private String courseTest;
	//定义课程的开课学期
	private String courseTime;
	//定义课程的课程类别
	private String courseKind;
	//定义课程的课程性质
	private String courseNature;
	//定义课程的考核方式
	private String courseExam;
	//定义课程的承担单位
	private String courseCollege;
	
	//定义Getters和Setters
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseScore() {
		return courseScore;
	}
	public void setCourseScore(String courseScore) {
		this.courseScore = courseScore;
	}
	public String getCourseTheory() {
		return courseTheory;
	}
	public void setCourseTheory(String courseTheory) {
		this.courseTheory = courseTheory;
	}
	public String getCourseTest() {
		return courseTest;
	}
	public void setCourseTest(String courseTest) {
		this.courseTest = courseTest;
	}
	public String getCourseTime() {
		return courseTime;
	}
	public void setCourseTime(String courseTime) {
		this.courseTime = courseTime;
	}
	public String getCourseKind() {
		return courseKind;
	}
	public void setCourseKind(String courseKind) {
		this.courseKind = courseKind;
	}
	public String getCourseNature() {
		return courseNature;
	}
	public void setCourseNature(String courseNature) {
		this.courseNature = courseNature;
	}
	public String getCourseExam() {
		return courseExam;
	}
	public void setCourseExam(String courseExam) {
		this.courseExam = courseExam;
	}
	public String getCourseCollege() {
		return courseCollege;
	}
	public void setCourseCollege(String courseCollege) {
		this.courseCollege = courseCollege;
	}
}
